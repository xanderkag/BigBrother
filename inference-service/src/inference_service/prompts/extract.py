"""Prompt template for /v1/extract.

Два режима:

  1. **Builtin** — наша инструкция, общая для всех типов документов
     (`build_builtin`). Используется когда админ ничего не настроил.

  2. **Override** — кастомная инструкция, заведённая через UI Document
     Type Registry (поле `llm_prompt`). Подмешиваем к ней технические
     требования к формату ответа (JSON, ключи extracted/confidence/issues
     и пр.) — иначе администратор каждый раз был бы вынужден
     дублировать «верни валидный JSON с ключами …».
"""

import json
from typing import Any

# Общая «техническая концовка» — что модель должна вернуть в JSON.
# Подмешивается и к builtin'у, и к override'у. Один источник правды
# для формата ответа.
_RESPONSE_CONTRACT = """Выводи строго валидный JSON в формате:
{{
  "extracted": <объект по схеме>,
  "confidence": <0..1, твоя общая уверенность в извлечённых полях>,
  "field_confidence": {{
      "<field_path>": <0..1>,
      ...
  }},
  "issues": [<строки с проблемами, может быть пустым>]
}}

ПОЛЕ `field_confidence` (F2 — per-field confidence) — ОБЯЗАТЕЛЬНОЕ:
Map от пути поля к уверенности (0..1). Указывай свою уверенность ОТДЕЛЬНО
по каждому ключевому полю. Это критично для SLAI matcher'а — он должен
знать какому полю можно доверять, а какому нет, чтобы не привязать
документ к неверной перевозке.

Минимальный набор путей которые ОБЯЗАТЕЛЬНО присутствуют в field_confidence:
  "number", "date", "seller.inn", "buyer.inn", "total_with_vat"
Плюс по возможности: "seller.name", "buyer.name", "vehicle.plate",
"items[*].name" (общая уверенность по всем строкам).

Значения интерпретируй так:
- 0.95-1.00 — поле распознано чётко, в документе явно
- 0.70-0.94 — есть мелкая неопределённость (плохой шрифт, частично OCR-шум)
- 0.40-0.69 — поле похоже распознано, но возможна ошибка в 1-2 символах
- 0.10-0.39 — поле распознано неуверенно, лучше переспросить оператора
- 0.00 — поле не нашлось (обычно тогда его вообще нет в extracted)

КРИТИЧЕСКИ ВАЖНО про формат ответа (F14):
- Твой ответ должен НАЧИНАТЬСЯ символом `{` и ЗАКАНЧИВАТЬСЯ символом `}`
- НЕ пиши вводных предложений ("Let me extract...", "I need to verify...")
- НЕ оборачивай в markdown ```json ... ```
- НЕ комментируй после JSON
- Если хочешь упомянуть проблемы — добавь в массив "issues" ВНУТРИ JSON"""

BUILTIN_TEMPLATE = """Ты извлекаешь структурированные поля из текста делового документа на русском языке.

Тип документа (подсказка): {hint}

Целевая JSON-схема (используй только эти поля, лишних не добавляй):
{schema}

Правила:
- Все суммы — числами без пробелов и без валюты ("15 000,50" → 15000.50).
- Все даты — в формате YYYY-MM-DD.
- ИНН — строка из 10 или 12 цифр.
- Если поле не нашлось в тексте — НЕ выдумывай, оставляй его пустым (отсутствует в JSON).
- Если есть подозрение на ошибку OCR (несвязные цифры, обрезанные слова) — добавляй описание в "issues".

{response_contract}

Текст документа:
\"\"\"
{text}
\"\"\"

{tail_reminder}"""


OVERRIDE_TEMPLATE = """{admin_instructions}

Тип документа: {hint}

Целевая JSON-схема (используй только эти поля, лишних не добавляй):
{schema}

{response_contract}

Текст документа:
\"\"\"
{text}
\"\"\"

{tail_reminder}"""


# Короткое напоминание ПОСЛЕ текста документа. Маленькие модели (phi4) теряют
# response-контракт, когда между ним и точкой генерации лежит 12 KB текста:
# на bench 2026-05-25 phi4 систематически возвращал поля БЕЗ обёртки
# `extracted` и под не-каноническими ключами (invoice_details.inn). Повтор у
# самого конца промпта резко повышает шанс корректной формы.
_TAIL_REMINDER = """НАПОМИНАНИЕ О ФОРМАТЕ ОТВЕТА (соблюдай строго):
- Ответ — ОДИН JSON-объект, начинается с `{` и заканчивается `}`.
- Все извлечённые поля клади ВНУТРЬ ключа "extracted". НЕ выноси number/date/
  seller/buyer на верхний уровень ответа — они должны быть в extracted.
- Используй РОВНО канонические ключи из схемы выше: seller.inn, buyer.inn,
  number, date, total, vat, items[]. НЕ придумывай свои обёртки
  (invoice_details, payment_details, invoice, party1/party2) — это ошибка.
- РОЛИ СТОРОН (не перепутай — частая ошибка):
    seller = сторона, ВЫСТАВИВШАЯ документ и ПОЛУЧАЮЩАЯ оплату — в документе
      помечена «Поставщик», «Продавец», «Получатель», «Исполнитель»,
      «Грузоотправитель». Её реквизиты — в seller.*.
    buyer = сторона, ПЛАТЯЩАЯ за документ — помечена «Покупатель»,
      «Плательщик», «Заказчик». Её реквизиты — в buyer.*.
  Реквизиты «Плательщика» всегда в buyer.*, «Поставщика»/«Получателя» —
  всегда в seller.*. НЕ меняй их местами."""


def build(
    text: str,
    schema: dict[str, Any],
    hint: str | None,
    prompt_override: str | None = None,
) -> str:
    """Construct the extract-prompt. Если задан `prompt_override` — он
    становится «системной частью» промпта (что считать продуктом), а
    технические требования к формату ответа подмешиваются автоматически.

    Truncate document text at 12 KB to keep token budget предсказуемым;
    реальный лимит у моделей варьируется (4k-128k), но 12 KB ~= 3000
    токенов, что безопасно для большинства open-source моделей."""
    common = {
        "hint": hint or "не указан",
        "schema": json.dumps(schema, ensure_ascii=False, indent=2),
        "text": text[:12000],
        "response_contract": _RESPONSE_CONTRACT,
        "tail_reminder": _TAIL_REMINDER,
    }
    if prompt_override:
        return OVERRIDE_TEMPLATE.format(
            admin_instructions=prompt_override.strip(),
            **common,
        )
    return BUILTIN_TEMPLATE.format(**common)


# --- Раздельные части для prompt caching (F8) ---
# Anthropic cache работает на префиксе. Если bake'ить всё в один user-message
# (как `build` выше), кэш будет работать только пока документ один и тот же —
# бесполезно. Чиним: статическая часть (instructions + schema + контракт)
# идёт в system-prompt с cache_control, динамическая (text документа) —
# в user-message. Тогда на каждый новый документ из 10 вызовов 9 raз
# получают cached input — экономия 90% на input tokens.

_STATIC_BUILTIN_HEADER = """Ты извлекаешь структурированные поля из текста российского делового документа.

ТИПИЧНЫЕ ТИПЫ ДОКУМЕНТОВ В РОССИЙСКОЙ ЛОГИСТИКЕ И БУХГАЛТЕРИИ:

1. Счёт на оплату (invoice) — содержит шапку «СЧЁТ № ... от ...», блок
   поставщика и покупателя с реквизитами (ИНН, КПП, расчётный счёт),
   таблицу позиций (наименование, кол-во, цена, сумма, НДС), итоговые
   суммы (без НДС, НДС, всего к оплате). Назначение — выставить
   платёжное требование. Не подтверждает поставку.

   ВАЖНО для invoice — извлекать банковские реквизиты поставщика:
     seller.bank          — наименование банка
     seller.bik           — БИК (9 цифр)
     seller.account       — расчётный счёт (20 цифр)
     seller.corr_account  — корреспондентский счёт банка
     seller.phone         — телефон в формате +7XXXXXXXXXX
   Эти поля критичны для SLAI matcher'а — он сверяет «тот ли поставщик»
   не только по ИНН но и по расч.счёту. Если в счёте есть «Банк получателя:
   ..., БИК: ..., р/с: ...» — обязательно извлеки в seller.* поля.

2. Универсальный передаточный документ — УПД (UPD) — комбинирует
   функции счёта-фактуры и накладной/акта. Шапка «УПД-номер от дата»,
   статус 1 (счёт-фактура + первичка) или 2 (только первичка). Поля
   продавца, покупателя, грузоотправителя, грузополучателя. Таблица
   позиций обычно содержит код, наименование, кол-во, ед. изм., цену
   без НДС, ставку НДС, сумму НДС, всего с НДС.

3. Товарно-транспортная накладная — ТТН (TTN) — оформляет перевозку
   груза автотранспортом. Содержит грузоотправителя, грузополучателя,
   перевозчика, госномер ТС (формат А123ВВ77 — буквы кириллицы из
   набора АВЕКМНОРСТУХ), водителя, маршрут (адрес погрузки и
   разгрузки), таблицу груза (наименование, кол-во, масса нетто/брутто).
   Это PII-чувствительный документ (ФИО водителя, паспорт).

4. CMR (международная ТТН) — для международных перевозок (24 графы по
   Конвенции МДП). Аналог ТТН для трансграничных грузов.

5. Акт выполненных работ — АКТ (AKT) — подтверждает оказание услуг.
   Шапка «АКТ № ... выполненных работ от ...», исполнитель, заказчик,
   договор-основание, таблица услуг (название, кол-во, ед.изм., цена,
   сумма). Подписи сторон. Используется в логистике для подтверждения
   транспортных услуг, экспедирования, погрузки.

6. Платёжное поручение (payment_order) — поручение банку перевести
   средства. Поля: плательщик с реквизитами (БИК, расч.счёт), получатель
   с реквизитами, назначение платежа, сумма, дата. БЕЗ items[]
   (это не товарный документ, items не извлекать).

7. Счёт-фактура (factInvoice) — структурно равен УПД статуса 1, но
   только бухгалтерская часть без передаточной. Распознать можно по
   заголовку «СЧЁТ-ФАКТУРА».

8. Коммерческий инвойс (commercial_invoice) — для ВЭД. Поля: exporter,
   consignee, HS-code (ТН ВЭД), incoterms, страна происхождения, валюта.

9. Упаковочный лист (packing_list) — описывает физическую упаковку:
   места, вес нетто/брутто, объём, габариты по позициям. Не содержит
   цен.

10. Коносамент B/L (bill_of_lading) — для морских перевозок. Поля:
    vessel, voyage, контейнер (ISO-6346 формат ABCD1234567).

11. ГТД / ДТ (customs_declaration) — таможенная декларация. Регномер,
    декларант, графы 11/25/31, пошлины.

12. Кассовый чек (cash_receipt) — ККТ по 54-ФЗ. ФН, ФД, ФП, позиции.

13. Договор (contract) и Спецификация — приложения к договору с
    перечнем поставок.

ПРАВИЛА ИЗВЛЕЧЕНИЯ:

- Все суммы — числами без пробелов и без валюты ("15 000,50" → 15000.50)
- Все даты — в формате YYYY-MM-DD
- ИНН — строка из 10 (юрлицо) или 12 (ИП) цифр. Удалить пробелы и дефисы
- КПП — 9 символов (NNNNCCNNN). Если в документе нет — не выдумывай
- Госномер ТС — кириллица в верхнем регистре, без пробелов (А123ВВ77)
- ФИО водителей и контактных лиц — оставлять как написано в документе
  (внешняя редакция через flag redact_pii — не наша забота на этом этапе)
- Если поле не нашлось — НЕ выдумывай, опускай его (отсутствует в JSON)
- РОЛИ СТОРОН (критично — не перепутай):
    seller = сторона, ВЫСТАВИВШАЯ документ и ПОЛУЧАЮЩАЯ оплату — в документе
      помечена как «Поставщик», «Продавец», «Получатель», «Исполнитель»,
      «Грузоотправитель». Её ИНН → seller.inn.
    buyer = сторона, ПЛАТЯЩАЯ за документ — помечена как «Покупатель»,
      «Плательщик», «Заказчик». Её ИНН → buyer.inn.
  НЕ меняй seller и buyer местами: ИНН «Плательщика» всегда в buyer.inn,
  ИНН «Поставщика»/«Получателя» всегда в seller.inn
- Если есть подозрение на ошибку OCR (несвязные цифры, обрезанные
  слова, странные подстановки) — добавляй описание в "issues"
- Многостраничные документы (УПД на 2-3 страницы) — извлекать ВСЕ
  позиции из таблицы, продолжающейся на следующую страницу
- НДС обычно 0%, 10% (продукты, лекарства), 20% (всё остальное).
  Если в строке указан НДС — извлекать в `vat_rate` как число процентов

ПРИМЕР ИЗВЛЕЧЕНИЯ (УПД):

Вход (фрагмент):
```
УНИВЕРСАЛЬНЫЙ ПЕРЕДАТОЧНЫЙ ДОКУМЕНТ № УПД-706/1 от 2026-05-13

Продавец: АО «РосТранс», ИНН/КПП: 7728168971/772801001
Покупатель: ООО «Складские Решения», ИНН/КПП: 7704211201/770401001
Грузоотправитель: тот же
Грузополучатель: ООО «Складские Решения»

№   Наименование               Кол-во Ед   Цена    НДС     Сумма
1   Масло сливочное 82.5% 180г    32 шт   145.00  464.00  5104.00
```

Выход (фрагмент JSON):
```json
{
  "document_type": "UPD",
  "number": "УПД-706/1",
  "date": "2026-05-13",
  "seller": {"name": "АО «РосТранс»", "inn": "7728168971", "kpp": "772801001"},
  "buyer": {"name": "ООО «Складские Решения»", "inn": "7704211201", "kpp": "770401001"},
  "items": [
    {"line_no": 1, "name": "Масло сливочное 82.5% 180г",
     "qty": 32, "unit": "шт", "price": 145.00, "vat_rate": 10, "total": 5104.00}
  ]
}
```

"""


def build_cacheable(
    text: str,
    schema: dict[str, Any],
    hint: str | None,
    prompt_override: str | None = None,
) -> tuple[str, str]:
    """Вернуть (system_prompt, user_prompt) для Anthropic API с prompt
    caching. system_prompt — статическая часть (один и тот же между
    вызовами для одного и того же hint+schema), user_prompt — только
    содержимое документа.

    Backend подставит `cache_control: {"type": "ephemeral"}` на
    system-блок. Cache hit срабатывает если предыдущий запрос с тем же
    system был < 5 минут назад.
    """
    schema_json = json.dumps(schema, ensure_ascii=False, indent=2)
    if prompt_override:
        system = (
            f"{prompt_override.strip()}\n\n"
            f"Тип документа: {hint or 'не указан'}\n\n"
            f"Целевая JSON-схема (используй только эти поля, лишних не добавляй):\n{schema_json}\n\n"
            f"{_RESPONSE_CONTRACT.replace('{{', '{').replace('}}', '}')}"
        )
    else:
        system = (
            f"{_STATIC_BUILTIN_HEADER}"
            f"Тип документа (подсказка): {hint or 'не указан'}\n\n"
            f"Целевая JSON-схема (используй только эти поля, лишних не добавляй):\n{schema_json}\n\n"
            f"{_RESPONSE_CONTRACT.replace('{{', '{').replace('}}', '}')}"
        )
    user = f'Текст документа:\n"""\n{text[:12000]}\n"""\n\n{_TAIL_REMINDER}'
    return system, user
