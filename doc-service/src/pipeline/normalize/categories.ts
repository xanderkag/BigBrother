/**
 * Категоризировать одну строку по name. Возвращает `other` если ничего
 * не подошло.
 */
export function categorizeName(name: string): Category {
  const lower = name.toLowerCase();
  let bestCat: Category = 'other';
  let bestHits = 0;
  for (const cat of CATEGORIES_BY_PRIORITY) {
    if (cat === 'other') continue;
    let hits = 0;
    for (const kw of KEYWORDS[cat]) {
      if (lower.includes(kw)) hits++;
    }
    if (hits > bestHits) {
      bestHits = hits;
      bestCat = cat;
    }
  }
  return bestCat;
}

/**
 * Применить categorization ко всем `items[]` в extracted. Не перетирает
 * существующие `category_hint` (LLM мог попасть на редких категориях).
 */
export function applyCategoryHints(
  extracted: Record<string, unknown> | null,
): Record<string, unknown> | null {
  if (!extracted || typeof extracted !== 'object') return extracted;
  const items = (extracted as { items?: unknown }).items;
  if (!Array.isArray(items) || items.length === 0) return extracted;

  let anyChanged = false;
  const newItems = items.map((raw) => {
    if (!raw || typeof raw !== 'object') return raw;
    const row = raw as Record<string, unknown>;
    if (typeof row.category_hint === 'string' && row.category_hint.length > 0 && row.category_hint !== 'other') {
      // Уважаем то что вернул LLM, если это не пустой `other`
      return row;
    }
    const name = typeof row.name === 'string' ? row.name : '';
    if (!name) return row;
    const cat = categorizeName(name);
    if (row.category_hint === cat) return row;
    anyChanged = true;
    return { ...row, category_hint: cat };
  });

  if (!anyChanged) return extracted;
  return { ...extracted, items: newItems };
}
