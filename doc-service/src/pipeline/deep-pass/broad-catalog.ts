const BY_SLUG = new Map(BROAD_TYPES.map((t) => [t.slug, t]));

/** Валидация slug'а широкой категории; невалидный → 'other'. */
export function normalizeBroadSlug(slug: string | null | undefined): BroadType {
  const clean = (slug ?? '').trim().toLowerCase().replace(/[^a-z0-9_]/g, '');
  return BY_SLUG.get(clean) ?? (BY_SLUG.get('other') as BroadType);
}

/** Текст словаря для промпта: `slug — label (hint)` по строке на категорию. */
export function broadCatalogText(): string {
  return BROAD_TYPES.map((t) => `${t.slug} — ${t.label} (${t.hint})`).join('\n');
}
