-- SLAI EOD-report 2026-05-17 — фиксы классификатора по P0 issues.
--
-- Issue #1: payment_order ложно срабатывает на 1С-счетах
-- ─────────────────────────────────────────────────────
-- В 1С-форме «Счёт на оплату» стандартный header содержит блок
-- «Образец заполнения платёжного поручения» с банковскими реквизитами
-- (БИК + к/с + р/с). Это **визуальный шаблон**, не реальное П/П.
--
-- Старый rule `БИК\s*\d{9}` для payment_order ловил эту шапку и
-- классифицировал документ как payment_order. На SLAI test-set
-- (15 счетов) это давало 15/15 false positives.
--
-- Решение: убираем bare-БИК паттерн. Реальные платёжки всегда имеют
-- «платежное поручение» в заголовке — это надёжный сигнал. Остальные
-- паттерны (П.П. №, Поступ. в банк плат., Списано со сч. плат.) —
-- fallback для повреждённых сканов где title не читается.
--
-- Issue #2: transport_request не матчит «ЗАЯВКА-ДОГОВОР № X»
-- ──────────────────────────────────────────────────────────
-- Старые keywords ждали «заявка на перевозку» / «заявка-договор на
-- перевозку». Реальные шаблоны клиентов используют «ЗАЯВКА-ДОГОВОР № X
-- от Y г. на оказание транспортно-экспедиционных услуг» — между
-- «заявка-договор» и «на» стоит «№ X от Y г.» (с цифрами/датой), а
-- глагол не «на перевозку» а «на оказание ... услуг».
--
-- Добавляем три паттерна:
--   1. `заявка-договор\s*№` — высокоспецифичный заголовок шаблона
--   2. `заявка-договор` + `транспортно-экспедиционн` рядом — fallback
--   3. `заявка\s+на\s+транспортн[а-я]+\s+услуг[а-я]+` — синонимичная форма
--
-- Старые паттерны (на перевозку / на автоперевозку) оставляем — их
-- могут использовать другие клиенты.

BEGIN;

-- ── Issue #1: payment_order — убираем bare БИК ──────────────────────
UPDATE document_types
SET classification_keywords = ARRAY[
    'платёжное\s+поручение',
    'платежное\s+поручение',
    '\bП\.?\s*П\.?\s*№',
    'Поступ\.\s+в\s+банк\s+плат\.',
    'Списано\s+со\s+сч\.\s+плат\.'
]::text[]
WHERE slug = 'payment_order';

-- ── Issue #2: transport_request — расширяем keywords ────────────────
UPDATE document_types
SET classification_keywords = ARRAY[
    -- Историческое: «заявка № X», «заявка на перевозку», etc.
    'заявка\s+(?:№|на\s+перевозку|на\s+транспортные\s+услуги|на\s+автоперевозку|на\s+транспортно-экспедиционн)',
    -- Старая форма: «заявка-договор на перевозку»
    'заявка-договор\s+на\s+перевозку',
    -- НОВОЕ: основной заголовок шаблона «ЗАЯВКА-ДОГОВОР № X»
    'заявка-договор\s*№',
    -- НОВОЕ: тело шаблона с «транспортно-экспедиционн» рядом с заявкой
    'заявк[аи][^\n]{0,100}транспортно-экспедиционн',
    -- НОВОЕ: синонимичные формы «заявка на оказание транспортных услуг»
    'заявка\s+на\s+оказание\s+транспортн'
]::text[]
WHERE slug = 'transport_request';

-- Sanity check — обновили ровно 2 строки.
DO $$
DECLARE
    affected int;
BEGIN
    SELECT count(*) INTO affected
    FROM document_types
    WHERE slug IN ('payment_order', 'transport_request');
    IF affected <> 2 THEN
        RAISE EXCEPTION 'Expected 2 rows for slugs (payment_order, transport_request), got %', affected;
    END IF;
END $$;

COMMIT;

