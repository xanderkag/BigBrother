-- Up Migration
--
-- BUG: JS regex `\b` (word boundary) работает только для ASCII-словесных
-- символов [A-Za-z0-9_]. Кириллические буквы НЕ считаются \w в V8.
-- Pattern `\bКОНТРАКТ\s+№` НИКОГДА не матчит «Контракт №» — \b не
-- срабатывает на границе со строки и кириллической буквой К.
--
-- Симптом обнаружен на реальном VED-кейсе EWL-AME/180723: contract.pdf
-- классифицировался как `price_list` потому что keyword `прайс-?лист`
-- (без \b) матчит «Прайс-листом» в теле документа, а ВСЕ contract
-- keywords с `\b` перед кириллицей молча не активировались.
--
-- ФИКС: заменяем `\b` (когда оно перед кириллицей) на `(?:^|\W)` —
-- эта группа матчит начало строки ИЛИ любой non-word char (включая
-- пробел / переносы). Match совместим с ASCII и Unicode, plus
-- захватывает 0 или 1 символ префикса (учитываем при анализе длины match).
--
-- Это touches только Cyrillic keywords. ASCII keywords (\bprice\s+list\b,
-- \bCMR\b, etc.) остаются как есть — `\b` для них работает.
--
-- Заодно добавляем top-priority anchor: `^\s*(Контракт|Договор)` (начало
-- документа) с весом 20.0 — это «убийца» false positive'ов для contract.
-- При совпадении в первых 500 chars × title-boost 1.5 = effective 30.0,
-- что заведомо больше любого другого keyword'а в системе (max 7).

-- ─── contract: переписать Cyrillic-keywords + добавить anchor ───────────
UPDATE document_types SET
  classification_keywords = ARRAY[
    -- Anchor: «Контракт/Договор» в самом начале документа (top priority)
    '^\s*(Контракт|Договор)\s+№?',
    -- Header patterns (Cyrillic): заменили \b на (?:^|\W)
    '(?:^|\W)ДОГОВОР\s+№',
    '(?:^|\W)КОНТРАКТ\s+№',
    '(?:^|\W)Предмет\s+(?:настоящего\s+)?[Дд]оговора(?:\W|$)',
    '(?:^|\W)Предмет\s+(?:настоящего\s+)?[Кк]онтракта(?:\W|$)',
    '(?:^|\W)Права\s+и\s+обязанности\s+[Сс]торон(?:\W|$)',
    '(?:^|\W)Срок\s+действия\s+[Дд]оговора(?:\W|$)',
    '(?:^|\W)Срок\s+действия\s+[Кк]онтракта(?:\W|$)',
    '(?:^|\W)Подписи\s+[Сс]торон(?:\W|$)',
    '(?:^|\W)Договор\s+поставки(?:\W|$)',
    '(?:^|\W)Контракт\s+поставки(?:\W|$)',
    '(?:^|\W)Договор\s+оказания\s+услуг(?:\W|$)',
    '(?:^|\W)Договор\s+подряда(?:\W|$)',
    '(?:^|\W)Договор\s+аренды(?:\W|$)',
    '(?:^|\W)Договор\s+купли-продажи(?:\W|$)',
    '(?:^|\W)Контракт\s+купли-продажи(?:\W|$)',
    'настоящий\s+(?:договор|контракт)\s+о\s+нижеследующем'
  ],
  classification_keyword_weights = ARRAY[
    20.00,  -- anchor «^Контракт/Договор» — заведомый победитель
    7.00, 7.00, 7.00, 7.00,          -- header patterns
    4.00, 4.00, 4.00, 4.00,          -- attribute patterns
    7.00, 7.00, 7.00, 7.00,          -- contract type patterns
    7.00, 7.00, 7.00,                -- more contract types
    4.00                              -- body-language pattern (weak signal)
  ]
WHERE slug = 'contract';

