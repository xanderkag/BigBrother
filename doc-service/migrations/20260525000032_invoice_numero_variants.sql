-- P0 (2026-05-20, фрахт-счета SLAI): счета вида «СЧЁТ № INV-2025-104»
-- классифицировались как unknown (type=null, 0 candidates) и не извлекались.
--
-- Причина: pdf-parse (pdfjs-dist) при извлечении текстового слоя
-- декомпозирует символ «№» (U+2116) в ASCII «No». В raw_text получается
-- «СЧЁТ No INV-2025-104», а keyword `сч[её]т\s+№` ждёт именно «№» — не
-- матчит. Та же проблема у «инвойс № …».
--
-- Решение: расширяем numero-токен до альтернации (?:№|no|n°|nº|#) — ловим
-- все варианты, которыми рендерится «номер» в реальных PDF:
--   №   — исходный символ (если текстовый слой его сохранил)
--   No  — декомпозиция pdfjs (case-insensitive 'i' флаг ловит No/NO/no)
--   N°  — латиница + degree sign
--   Nº  — латиница + ordinal indicator
--   #   — англоязычные шаблоны
--
-- Веса не трогаем — структура и порядок keyword'ов сохранены 1:1, меняется
-- только numero-часть в двух signature-паттернах.

BEGIN;

UPDATE document_types
SET classification_keywords = ARRAY[
    '(?:^|\W)сч[её]т\s+на\s+оплату(?:\W|$)',
    '(?:^|\W)сч[её]т\s+(?:№|no|n°|nº|#)',
    '(?:^|\W)сч[её]т-оферт[ау]?(?:\W|$)',
    '(?:^|\W)инвойс\s+(?:№|no|n°|nº|#)',
    '(?:^|\W)инвойс\s+на\s+оплату(?:\W|$)'
]::text[]
WHERE slug = 'invoice';

COMMIT;

